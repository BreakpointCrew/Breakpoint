package cz.GravelCZLP.Breakpoint.exceptions;

public class NotStaffException extends Exception {

	private static final long serialVersionUID = 2240716299576992748L;

	public NotStaffException() {
		super();
	}

	public NotStaffException(String arg0) {
		super(arg0);
	}

	public NotStaffException(Throwable arg0) {
		super(arg0);
	}

	public NotStaffException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}
}
